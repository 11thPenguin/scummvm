#include "matrix4.h"

Matrix4::Matrix4() {
	_pos.set(0.f, 0.f, 0.f);
	_rot.setAsIdentity();
}

void Matrix4::translate(float x, float y, float z) {
	Vector3d v;

	v.set(x, y, z);
	_rot.transform(&v);
	_pos += v;
}

